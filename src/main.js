// Plugins
import { registerPlugins } from '@/plugins'

// Components
import App from './App.vue'

// Composables
import { createApp } from 'vue'

const app = createApp(App)

registerPlugins(app)

app.mount('#app')

import { VueCookies } from 'vue-cookies'
app.use(VueCookies)

app.config.globalProperties.window = window
